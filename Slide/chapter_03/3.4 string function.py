#---------------------------------------------------
#* การตัดช่องว่าง หน้า-หลัง
#* ในการทำงาน บางครั้งเวลาเราเปรียบเทียบ string ถ้ามีช่องว่าง หน้าหรือหลังจะมองว่าไม่เท่ากัน
#* strip() ตัดช่องว่าง หน้า หลัง
#* rstrip() ตัดช่องว่าง หลัง
#* lstrip() ตัดช่องว่าง หน้า
#---------------------------------------------------

favorite_language = 'python ' 
print(favorite_language+':')
print(favorite_language.rstrip()+':')

favorite_language = ' python '
print(':'+favorite_language)
print(':'+favorite_language.lstrip())

