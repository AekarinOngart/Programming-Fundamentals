#---------------------------------------------------
#*การรวมชุด string ที่คั่นด้วย , จะใช้ join
#---------------------------------------------------

# .join() with lists
numList = ['1', '2', '3', '4']
separator = ', '
print(separator.join(numList))

s1 = 'abc'
s2 = '123'

# each element of s2 is separated by s1
# '1'+ 'abc'+ '2'+ 'abc'+ '3'
print('s1.join(s2):', s1.join(s2))

# each element of s1 is separated by s2
# 'a'+ '123'+ 'b'+ '123'+ 'b'
print('s2.join(s1):', s2.join(s1))

